package com.apachepoi;

public class Movie
{
	private String id;
	private String name;
	private String type;
	public String getId()
	{
		return id;
	}
	public void setId(String d)
	{
		this.id = d;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getType()
	{
		return type;
	}
	public void setType(String type)
	{
		this.type = type;
	}
}
